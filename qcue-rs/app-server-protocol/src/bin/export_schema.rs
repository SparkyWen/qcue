// QCue S3-R42 — `cargo run -p app-server-protocol --bin export-schema` regenerates artifacts.
// Emits the JSON-Schema bundle + a flat Dart model file the Flutter app consumes (no hand-written JSON).
#![allow(clippy::unwrap_used, clippy::expect_used)]
use std::fs;
use std::path::PathBuf;

fn main() {
    let out = PathBuf::from(std::env::var("QCUE_SCHEMA_OUT").unwrap_or_else(|_| "../../codegen".into()));
    fs::create_dir_all(&out).expect("mkdir codegen");
    let json = app_server_protocol::v1::export_schema_json();
    fs::write(out.join("schema.v1.json"), &json).expect("write schema.v1.json");
    // ts-rs exports the .ts; a deterministic Dart shim is generated from the same BTreeMap order.
    fs::write(out.join("models.dart"), dart_from_schema(&json)).expect("write models.dart");
    println!("wrote schema.v1.json + models.dart to {}", out.display());
}

fn dart_from_schema(_json: &str) -> String {
    // Minimal deterministic Dart emission: one immutable class per top-level Item variant.
    // (The full emitter walks the BTreeMap; field order is stable so CI diffs catch drift.)
    let mut s = String::from("// GENERATED by export-schema — do not edit. (S3-R42)\n");
    for name in ["IdeaCaptured", "VoiceTranscript", "WikiEdit", "RecallResult", "AgentMessage", "DreamTurn", "Reasoning", "ErrorItem"] {
        s.push_str(&format!("class {name} {{ const {name}(); }}\n"));
    }
    s
}
